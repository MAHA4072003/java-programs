class whileloop8
{
	public static void main(String[]args)
	{
		int i=555;
		int sum=0;
		while(i<=777)
		{
			if(i%2!=0)
			{
				sum=sum+i;			
			}
			i++;		
		}
		System.out.println(sum);                                                                                                                            
	}
}
			
		
